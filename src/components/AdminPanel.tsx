              <div>
                <input
                  type="password"
                  placeholder="Chave de Acesso Admin..."
                  required
                  value={passwordInput}
                  onChange={(e) => setPasswordInput(e.target.value)}
                  className="w-full text-center bg-[#070C15] border border-slate-800 focus:border-red-500 text-white text-sm p-3 rounded-lg outline-none font-mono tracking-widest placeholder:tracking-normal"
                  autoFocus
                />
              </div>

