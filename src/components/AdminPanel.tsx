    e.preventDefault();
    const form = e.currentTarget as HTMLFormElement;
    const nameInput = form.elements.namedItem('vipName') as HTMLInputElement;
    if (!nameInput.value.trim()) return;

    const today = new Date();
    const dateStr = `${String(today.getDate()).padStart(2, '0')}/${String(today.getMonth() + 1).padStart(2, '0')}/${today.getFullYear()}`;
    const newMember = {
      name: nameInput.value.trim(),
      date: dateStr,
      status: 'Ativo'
    };

    const updated = [newMember, ...vipMembers];
    try {
      const response = await fetch('/api/vip-members', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'x-admin-password': verifiedPassword
        },
        body: JSON.stringify(updated)
      });
      if (response.ok) {
        setVipMembers(updated);
        localStorage.setItem('west_store_vip_members', JSON.stringify(updated));
        nameInput.value = '';
        triggerNotification('Assinante VIP cadastrado!');
        onNotifyChange();
      } else {
        const err = await response.json();
        alert(err.error || 'Erro ao sincronizar membro VIP.');
      }
    } catch (err) {
      console.error(err);
      alert('Erro de conexão ao salvar membro VIP.');
    }
