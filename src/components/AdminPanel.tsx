  // Forms and settings managers
  const [showAddForm, setShowAddForm] = useState(false);
  const [successMsg, setSuccessMsg] = useState('');

  // New Supabase Image uploading & tracking states
  const [imageUrlInput, setImageUrlInput] = useState('');
  const [isUploading, setIsUploading] = useState(false);
  const [isDragging, setIsDragging] = useState(false);
  const [supabaseStatus, setSupabaseStatus] = useState<{ enabled: boolean, url: string | null, hasKey: boolean }>({ enabled: false, url: null, hasKey: false });

  // New Jersey Form Values
  const [newId, setNewId] = useState('');
  const [newName, setNewName] = useState('Novo Manto Oficial');
  const [newTeam, setNewTeam] = useState<TeamName>('São Paulo FC');
  const [newType, setNewType] = useState<KitType>('HomeKit');
  const [newSeason, setNewSeason] = useState('24/25');
  const [newPriceRetail, setNewPriceRetail] = useState(129.90);
  const [newPriceWholesale, setNewPriceWholesale] = useState(119.90);
  const [newIsRetro, setNewIsRetro] = useState(false);

  // Form stock values
  const [stockP, setStockP] = useState(12);
  const [stockM, setStockM] = useState(15);
  const [stockG, setStockG] = useState(8);
  const [stockGG, setStockGG] = useState(6);

  // Custom design values
  const [primaryColor, setPrimaryColor] = useState('#FFFFFF');
  const [secondaryColor, setSecondaryColor] = useState('#E74C3C');
  const [accentColor, setAccentColor] = useState('#2C3E50');
  const [pattern, setPattern] = useState<KitDesign['pattern']>('solid');
  const [patternSecondaryColor, setPatternSecondaryColor] = useState('#2C3E50');
  const [collarColor, setCollarColor] = useState('#E74C3C');
  const [sleeveColor, setSleeveColor] = useState('#FFFFFF');
  const [crestText, setCrestText] = useState<'SPFC' | 'CRF' | 'CP' | ''>('SPFC');
  const [sponsorName, setSponsorName] = useState('SUPERBET');
  const [sponsorColor, setSponsorColor] = useState('#1A3050');

  // Simulated VIP subscribers log
  const [vipMembers, setVipMembers] = useState<{name: string, date: string, status: string}[]>(() => {
    const saved = localStorage.getItem('west_store_vip_members');
    if (saved) return JSON.parse(saved);
    return [
      { name: 'Marco Latapiat', date: '15/06/2026', status: 'Ativo' },
      { name: 'Gabriel Souza', date: '14/06/2026', status: 'Ativo' },
      { name: 'Amanda Oliveira', date: '13/06/2026', status: 'Ativo' },
      { name: 'Bruno Mendes', date: '12/06/2026', status: 'Ativo' }
    ];
  });

  // Simulated active coupons code list
  const [coupons, setCoupons] = useState<{code: string, discount: string, type: string, status: string}[]>([
    { code: 'WEST10', discount: '10%', type: 'Fixo', status: 'Ativo' },
    { code: 'VIPWEST', discount: '15%', type: 'VIP', status: 'Ativo' },
    { code: 'FRETEGRATIS', discount: 'R$ 20', type: 'Frete', status: 'Ativo' },
    { code: 'ATACADO5', discount: '5%', type: 'Volume', status: 'Ativo' }
  ]);
  const [newCouponCode, setNewCouponCode] = useState('');

  // Sincronizar dados do servidor para paridade perfeita
  const fetchAdminDataFromServer = async () => {
    try {
      const configRes = await fetch('/api/site-config');
      if (configRes.ok) {
        const configData = await configRes.json();
        setSiteConfig(configData);
      }
      const catalogRes = await fetch('/api/catalog');
      if (catalogRes.ok) {
        const catalogData = await catalogRes.json();
        setJerseys(catalogData);
      }
      const vipsRes = await fetch('/api/vip-members');
      if (vipsRes.ok) {
        const vipsData = await vipsRes.json();
        setVipMembers(vipsData);
      }
      const couponsRes = await fetch('/api/coupons');
      if (couponsRes.ok) {
        const couponsData = await couponsRes.json();
        setCoupons(couponsData);
      }
      const statusRes = await fetch('/api/supabase-status');
      if (statusRes.ok) {
        const statusData = await statusRes.json();
        setSupabaseStatus(statusData);
      }
    } catch (e) {
      console.error("Erro ao atualizar dados do servidor:", e);
    }
  };

  const processImageUpload = async (file: File) => {
    setIsUploading(true);
    const reader = new FileReader();
    reader.onloadend = async () => {
      try {
        const base64Data = reader.result as string;
        const response = await fetch('/api/upload', {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
            'x-admin-password': verifiedPassword
          },
          body: JSON.stringify({
            fileName: `jersey-${Date.now()}-${file.name.replace(/[^a-zA-Z0-9.-]/g, "_")}`,
            base64Data,
            contentType: file.type
          })
        });

        if (response.ok) {
          const resData = await response.json();
          setImageUrlInput(resData.imageUrl);
          triggerNotification('Foto carregada no Supabase Storage com sucesso!');
        } else {
          const errData = await response.json();
          alert(errData.error || 'Erro ao carregar imagem para o Supabase Storage.');
        }
      } catch (err: any) {
        console.error(err);
        alert('Erro ao carregar arquivo.');
      } finally {
        setIsUploading(false);
      }
    };
    reader.readAsDataURL(file);
  };

  const handleImageFileUpload = (e: React.ChangeEvent<HTMLInputElement>) => {
    const file = e.target.files?.[0];
    if (file) {
      processImageUpload(file);
    }
  };

  const handleLogin = async (e: React.FormEvent) => {
    e.preventDefault();
    try {
      const response = await fetch('/api/auth/login', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ password: passwordInput })
      });

      if (response.ok) {
        setIsAuthenticated(true);
        setVerifiedPassword(passwordInput);
        setLoginError(false);
        await fetchAdminDataFromServer();
      } else {
        setLoginError(true);
      }
    } catch (err) {
      console.error("Erro na autenticação:", err);
      setLoginError(true);
    }
  };

  const handleSaveConfig = async (e: React.FormEvent) => {
    e.preventDefault();
    try {
      const response = await fetch('/api/site-config', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'x-admin-password': verifiedPassword
        },
        body: JSON.stringify(siteConfig)
      });
      if (response.ok) {
        saveSiteConfig(siteConfig);
        onNotifyChange();
        triggerNotification('Configurações salvas e aplicadas!');
      } else {
        const err = await response.json();
        alert(err.error || 'Erro ao registrar alterações.');
      }
    } catch (err) {
      console.error(err);
      alert('Erro de conexão ao servidor.');
    }
  };

  const triggerNotification = (msg: string) => {
    setSuccessMsg(msg);
    setTimeout(() => setSuccessMsg(''), 4000);
  };

  const handleDeleteJersey = async (id: string) => {
    if (window.confirm('Excluir esta camiseta do catálogo público?')) {
      const updated = jerseys.filter(j => j.id !== id);
      try {
        const response = await fetch('/api/catalog', {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
            'x-admin-password': verifiedPassword
          },
          body: JSON.stringify(updated)
        });
        if (response.ok) {
          setJerseys(updated);
          saveJerseyCatalog(updated);
          onNotifyChange();
          triggerNotification('Produto removido com sucesso!');
        } else {
          const err = await response.json();
          alert(err.error || 'Erro ao excluir.');
        }
      } catch (err) {
        console.error(err);
      }
    }
  };

  const currentDesignDraft: KitDesign = {
    primaryColor,
    secondaryColor,
    accentColor,
    pattern,
    patternSecondaryColor,
    collarColor,
    sleeveColor,
    hasCrestText: crestText || undefined,
    sponsorName,
    sponsorColor,
  };

  const handleTeamChange = (team: TeamName) => {
    setNewTeam(team);
    if (team === 'São Paulo FC') {
      setCrestText('SPFC');
      setPrimaryColor('#FFFFFF');
      setSecondaryColor('#E74C3C');
      setAccentColor('#2C3E50');
      setCollarColor('#E74C3C');
      setSleeveColor('#FFFFFF');
      setSponsorName('SUPERBET');
      setSponsorColor('#111111');
    } else if (team === 'Flamengo') {
      setCrestText('CRF');
      setPrimaryColor('#111111');
      setSecondaryColor('#C0392B');
      setAccentColor('#FFFFFF');
      setCollarColor('#111111');
      setSleeveColor('#C0392B');
      setSponsorName('PIXBET');
      setSponsorColor('#FFFFFF');
    } else if (team === 'Corinthians') {
      setCrestText('CP');
      setPrimaryColor('#FFFFFF');
      setSecondaryColor('#111111');
      setAccentColor('#C0392B');
      setCollarColor('#111111');
      setSleeveColor('#111111');
      setSponsorName('VAIDEBET');
      setSponsorColor('#111111');
    }
  };

  const handleUpdateStock = async (id: string, size: 'P' | 'M' | 'G' | 'GG', newStock: number) => {
    const updated = jerseys.map(j => {
      if (j.id === id) {
        return {
          ...j,
          stock: {
            P: j.stock?.P ?? 10,
            M: j.stock?.M ?? 10,
            G: j.stock?.G ?? 10,
            GG: j.stock?.GG ?? 10,
            [size]: newStock
          }
        };
      }
      return j;
    });

    try {
      const response = await fetch('/api/catalog', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'x-admin-password': verifiedPassword
        },
        body: JSON.stringify(updated)
      });
      if (response.ok) {
        setJerseys(updated);
        saveJerseyCatalog(updated);
        onNotifyChange();
      }
    } catch (err) {
      console.error(err);
    }
  };

  const handleAddJersey = async (e: React.FormEvent) => {
    e.preventDefault();

    const finalId = newId.trim() || `${newTeam.toLowerCase().substring(0, 3)}-${newType.toLowerCase()}-${Date.now().toString().substring(8)}`;

    const newJersey: JerseyKit = {
      id: finalId,
      name: newName,
      team: newTeam,
      type: newType,
      season: newSeason,
      priceRetail: newPriceRetail,
      priceWholesale: newPriceWholesale,
      isRetro: newIsRetro,
      sponsor: sponsorName || undefined,
      design: currentDesignDraft,
      imageUrl: imageUrlInput || undefined,
      stock: { P: stockP, M: stockM, G: stockG, GG: stockGG }
    };

    const updated = [newJersey, ...jerseys];

    try {
      const response = await fetch('/api/catalog', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'x-admin-password': verifiedPassword
        },
        body: JSON.stringify(updated)
      });
      if (response.ok) {
        setJerseys(updated);
        saveJerseyCatalog(updated);
        onNotifyChange();

        setShowAddForm(false);
        setNewId('');
        setImageUrlInput('');
        triggerNotification('Camisa cadastrada com êxito!');
      } else {
        const err = await response.json();
        alert(err.error || 'Erro ao adicionar.');
      }
    } catch (err) {
      console.error(err);
    }
  };

